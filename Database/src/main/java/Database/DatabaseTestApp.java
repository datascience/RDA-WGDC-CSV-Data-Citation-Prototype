package Database;


import CSVTools.CSV_API;
import CSVTools.Column;

import java.io.IOException;
import java.sql.SQLException;
import java.util.Map;

/**
 * Hello world!
 */
public class DatabaseTestApp {
    public static void main(String[] args) {
        System.out.println("CSV Test Application");
        String databaseName = "CITATION_DB";
        String tableName = "MSD100k-Test";
        String path = "/media/Data/Datasets/";
        String csvFileName = "msd1k.csv";
        boolean calulateHashColumn = false;

        CSV_API csv;
        csv = new CSV_API();
        String[] csvColumnHeaders = csv.getArrayOfHeadersCSV(path + csvFileName);
        Column[] meta = null;
        try {
            csv.readWithCsvListReaderAsStrings(path + csvFileName);
            meta = csv.analyseColumns(true, path + csvFileName);
        } catch (Exception e) {
            e.printStackTrace();
        }


        DatabaseTools db = new DatabaseTools(databaseName);
        Map<String, String> names = null;
        try {
            db.createSimpleDBFromCSV(meta, tableName, calulateHashColumn);
            db.insertCSVDataIntoDB(path + csvFileName, tableName, true, calulateHashColumn);
            db.getRowCount(tableName);
            names = db.getTableColumnMetadata(tableName);

        } catch (SQLException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }


        for (Map.Entry<String, String> entry : names.entrySet()) {
            String key = entry.getKey();
            Object value = entry.getValue();
            // ...
        }

        String databaseTestName = db.getAvailableDatabases().get(0);
        db.getAvailableTablesFromDatabase(databaseTestName);


    }
}
